def hotel_chatbot():
    print("Hotel Chatbot: Hello! How can I assist you today?")
    while True:
        user_input = input("You: ")
        if user_input.lower() in ["exit", "bye", "goodbye"]:
            print("Hotel Chatbot: Thank you for chatting with us. Have a great day!")
            break
        if "timings" in user_input.lower():
            response = random.choice(responses["hotel_timings"])
        elif "food" in user_input.lower():
            response = random.choice(responses["food_options"])
