class BusSchedule:
    def __init__(self):
        self.schedule = {
            ("Place1", "Place2"): ["08:00 AM", "11:00 AM", "03:00 PM"],
            ("Place2", "Place3"): ["09:30 AM", "12:30 PM", "04:30 PM"],
            # Add more routes and timings as needed
        }

    def get_bus_timings(self, start_place, destination):
        route = (start_place, destination)
        if route in self.schedule:
            return self.schedule[route]
        else:
            return None

# Example usage
bus_schedule = BusSchedule()
